# Создание множества
words_set = {"apple", "banana", "cherry", "date"}

# Сортировка по длине строк
sorted_by_length = sorted(words_set, key=len)

print("Отсортированный список по длине:", sorted_by_length)  # Вывод: ['date', 'apple', 'banana', 'cherry']
